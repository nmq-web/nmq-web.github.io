// Navigation
function openMenu() {
    document.getElementsByClassName("navigation-4")[0].style.display = "block"
    document.getElementsByClassName("navigation-5")[0].style.display = "block"
}

function closeMenu() {
    document.getElementsByClassName("navigation-4")[0].style.display = "none"
    document.getElementsByClassName("navigation-5")[0].style.display = "none"
}

// Back To Top
function openBacktotop() {
    document.getElementsByClassName("back-to-top")[0].style.display = "block"
    document.getElementsByClassName("back-to-top-open")[0].style.display = "none"
}

function closeBacktotop() {
    document.getElementsByClassName("back-to-top")[0].style.display = "none"
    document.getElementsByClassName("back-to-top-open")[0].style.display = "block"

}